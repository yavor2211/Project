# Да се напише програма, която проверява дали въведеното от потребителя число
number = int(input())
# е в интервала [-100, 100] и е различно от 0 и извежда "Yes",
# ако отговаря на условията, или "No" ако е извън тях.

if number >= -100 and number <= 100 and number != 0:
    print('Yes')
else:
    print('No')